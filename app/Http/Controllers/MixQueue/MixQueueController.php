    protected function queryModifier($Query)
    {
        if (isset($this->RequestOptions->patient_id)) {
            $Query->where('patient_id', $this->RequestOptions->patient_id);
        }
        $Query->whereHas('compounds', function ($innerQuery2) {
            $innerQuery2->whereHas('vials', function ($innerQuery3) {
                $innerQuery3->where('postponed', 'T');
            });
        });
        return $Query;
    }

    protected function queryWith($Query)
    {
        return $Query->with('patient:firstname,lastname,mi,dob,chart,patient_id', 'provider:first,last,mi,phone,displayname,suffix,provider_id,licensenumber,providerNum,deaNum', 'compounds', 'prescription', 'prescription.user:user_id,displayname', 'purchaseOrder.account:account_id,account_number,name');
    }

    protected function finalize($Object)
    {
        $Object = $Object->toArray();

        $vials = $Object['vials'];

        $Object['name'] = count($vials) == 0 ? 'unnamed' : $vials[0]['name'];
        unset($Object['vials']);
        return $Object;
    }
